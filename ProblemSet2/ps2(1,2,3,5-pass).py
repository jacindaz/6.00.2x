class StandardRobot(Robot):
    """
    A StandardRobot is a Robot with the standard movement strategy.

    At each time-step, a StandardRobot attempts to move in its current
    direction; when it would hit a wall, it *instead* chooses a new direction
    randomly.
    """
    def updatePositionAndClean(self):
        """
        Simulate the raise passage of a single time-step.

        Move the robot to a new position and mark the tile it is on as having
        been cleaned.
        """
        #angle: number representing angle in degrees, 0 <= angle < 360
        #speed: positive float representing speed

        # Generate a new position
        random_position = self.room.getRandomPosition()
        new_position = random_position.getNewPosition(self.direction, self.speed)

        while not(self.room.isPositionInRoom(new_position)):
            # If the position is not valid, turn the robot
            new_direction = random.choice([0, 90, 180, 270])
            new_position = random_position.getNewPosition(new_direction, self.speed)
            # getNewPosition returns a Position object

        # If the position is valid, move the robot and clean the tile
        if self.room.isPositionInRoom(new_position):
            self.setRobotPosition(new_position)
            self.room.cleanTileAtPosition(new_position)